/**
 * Simple class that holds red,green and blue values.
 */
public class Pixel {
    public int Red;
    public int Green;
    public int Blue;

    /**
     * Simple constructor that sets 3 color.
     * @param red Red.
     * @param green Green.
     * @param blue Blue.
     */
    public Pixel(int red, int green, int blue) {
        Red = red;
        Green = green;
        Blue = blue;
    }
}
